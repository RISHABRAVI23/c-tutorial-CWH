#include <stdio.h>

int main(int argc, char const *argv[])
{
	/*
		A C program is made up of parts called tokens. These tokens include:
		1) Keywords
		2) Identifiers
		3) Constants
		4) String Literals
		5) Symbols (Like Operators)

		So here "return", "printf('Hello World\n')"and so on are all tokens.

		There has to be atleast 1 whitespace between 2 tokens for e.g between "return" and "0" there should be a whitespace.
	*/
	printf("Hello World\n");

	// scanf("%d", &a)
	return 0;
}
